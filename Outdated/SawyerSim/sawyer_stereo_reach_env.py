from __future__ import annotations

from typing import Any

import numpy as np
import numpy.typing as npt
from gymnasium.spaces import Box
from scipy.spatial.transform import Rotation

from Outdated.CustomMetaworld.metaworld.asset_path_utils import full_V3_path_for
from Outdated.SawyerSim.sawyer_base_env import RenderMode, SawyerXYZEnv
from Outdated.CustomMetaworld.metaworld.types import InitConfigDict
from Outdated.CustomMetaworld.metaworld.utils import reward_utils

class SawyerReachEnvV3(SawyerXYZEnv):
    def __init__(
        self,
        render_mode: RenderMode | None = None,
        camera_pairs = # Reference xyz_base for camera names
            [("stereo_left1", "stereo_right1"), 
            ("stereo_left2", "stereo_right2"),
            ("stereo_left3", "stereo_right3")], 
        camera_id: int | None = None,
        reward_function_version: str = "v2",
        
        img_height: int = 64,
        img_width: int = 64,
        discount: float = 0.99,
        max_path_length: int = 300, # Horizon
        obs_space: Box | None = None,
        action_space: Box | None = None
    ) -> None:
        goal_low = (-0.1, 0.8, 0.05)
        goal_high = (0.1, 0.9, 0.3)
        hand_low = (-0.5, 0.40, 0.05)
        hand_high = (0.5, 1, 0.5)
        obj_low = (-0.1, 0.6, 0.02)
        obj_high = (0.1, 0.7, 0.02)
        self.goal_space = Box(np.array(goal_low), np.array(goal_high), dtype=np.float32)
        self.max_path_length = max_path_length

        super().__init__(
            hand_low=hand_low,
            hand_high=hand_high,
            render_mode=render_mode,
            camera_pairs=camera_pairs,
            camera_id=camera_id,
            img_height=img_height,
            img_width=img_width,
            discount=discount
        )
        self.reward_function_version = reward_function_version

        self.init_config: InitConfigDict = {
            "obj_init_angle": 0.3,
            "obj_init_pos": np.array([0.0, 0.6, 0.02]),
            "hand_init_pos": np.array([0.0, 0.6, 0.2]),
        }

        self.goal = np.array([-0.1, 0.8, 0.2])

        self.obj_init_angle = self.init_config["obj_init_angle"]
        self.obj_init_pos = self.init_config["obj_init_pos"]
        self.hand_init_pos = self.init_config["hand_init_pos"]

        self._random_reset_space = Box(
            np.hstack((obj_low, goal_low)),
            np.hstack((obj_high, goal_high)),
            dtype=np.float32,
        )
        
        # OVERRIDE the base env
        self.observation_space = obs_space
        self.action_space = action_space

    @property
    def model_name(self) -> str:
        return full_V3_path_for("sawyer_xyz/sawyer_reach_v3.xml")
    
    def _get_state_obs():
        return None

    def evaluate_state(self, obs: npt.NDArray[np.float64] | None, action: npt.NDArray[np.float32] | None) -> tuple[float, dict[str, Any]]:
        reward, reach_dist, in_place = self.compute_reward()
        success = float(reach_dist <= 0.05)

        info = {
            "success": success,
            "near_object": reach_dist,
            "grasp_success": 1.0,
            "grasp_reward": reach_dist,
            "in_place_reward": in_place,
            "obj_to_target": reach_dist,
            "unscaled_reward": reward,
        }

        return reward, info

    def _get_pos_objects(self) -> npt.NDArray[Any]:
        return self.get_body_com("obj")

    def _get_quat_objects(self) -> npt.NDArray[Any]:
        geom_xmat = self.data.geom("objGeom").xmat.reshape(3, 3)
        return Rotation.from_matrix(geom_xmat).as_quat()

    def fix_extreme_obj_pos(self, orig_init_pos: npt.NDArray[Any]) -> npt.NDArray[Any]:
        # This is to account for meshes for the geom and object are not
        # aligned. If this is not done, the object could be initialized in an
        # extreme position
        diff = self.get_body_com("obj")[:2] - self.get_body_com("obj")[:2]
        adjusted_pos = orig_init_pos[:2] + diff
        # The convention we follow is that body_com[2] is always 0,
        # and geom_pos[2] is the object height
        return np.array(
            [adjusted_pos[0], adjusted_pos[1], self.get_body_com("obj")[-1]]
        )

    def reset_model(self) -> npt.NDArray[np.float32]:
        self._reset_hand()
        self._target_pos = self.goal.copy()
        self.obj_init_pos = self.fix_extreme_obj_pos(self.init_config["obj_init_pos"])
        self.obj_init_angle = self.init_config["obj_init_angle"]

        goal_pos = self._get_state_rand_vec()
        self._target_pos = goal_pos[3:]
        while np.linalg.norm(goal_pos[:2] - self._target_pos[:2]) < 0.15:
            goal_pos = self._get_state_rand_vec()
            self._target_pos = goal_pos[3:]
        self._target_pos = goal_pos[-3:]
        self.obj_init_pos = goal_pos[:3]
        self._set_obj_xyz(self.obj_init_pos)

        self.model.site("goal").pos = self._target_pos
        self.maxReachDist = np.linalg.norm(self.init_tcp - np.array(self._target_pos))

        return self._get_img_obs()
    
    def compute_reward(self) -> tuple[float, float, float]:
        assert self._target_pos is not None
        if self.reward_function_version == "v2":
            _TARGET_RADIUS: float = 0.05
            tcp = self.tcp_center
            target = self._target_pos
            tcp_to_target = float(np.linalg.norm(tcp - target))
            
            in_place_margin = float(np.linalg.norm(self.hand_init_pos - target))
            in_place = reward_utils.tolerance(
                tcp_to_target,
                bounds=(0, _TARGET_RADIUS),
                margin=in_place_margin,
                sigmoid="long_tail",
            )

            return (10 * in_place, tcp_to_target, in_place)